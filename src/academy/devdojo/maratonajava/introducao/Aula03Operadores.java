package academy.devdojo.maratonajava.introducao;

public class Aula03Operadores {
    public static void main(String[] args) {
        int num1 = 10;
        String num2 = "20";
        System.out.println(num1 + num2);

        /*

        Operadores aritméticos
            + -> soma ou concatenação
             -> se a declaração começar com uma String
             , for uma String, o sinal de + e um número ou se for uma declaração com várias Strings
             , o sinal de + fará uma concatenação e não uma soma

            - -> subtração
            / -> divisão
            * -> multiplicação
            % -> módulo ou resto da divisão

         */

        /*

        Operadores relacionais

        <  -> menor
        >  -> maior
        <= -> menor ou igual
        >= -> maior ou igual
        == -> igualdade
        != -> diferença

         */

        /*

        Operadores Lógicos

        && -> and
        || -> or
        !  -> not

         */

        /*

        = -> atribuição
        +=
        -=
        *=
        /=
        %=

        ++ -> incremento
        -- -> decremento
         */
    }
}
